import os

base_dir = "/Users/zhangshaowei/workspace/gemini_ws/security_master/security-master-skill/knowledge/cases/vertical/infosec"
dir_09 = os.path.join(base_dir, "09-devsecops")
dir_10 = os.path.join(base_dir, "10-emerging")

def write_files(directory, cases):
    for case in cases:
        filepath = os.path.join(directory, case['filename'])
        content = f"""# {case['title']}
- **所属领域**: 信息安全
- **威胁类型**: {case['threat']}
- **事件回顾**: {case['review']}
- **Red View (利用路径)**: {case['red_view']}
- **Blue View (防御缺失)**: {case['blue_view']}
- **策略借鉴**: {case['strategy']}
"""
        with open(filepath, 'w', encoding='utf-8') as f:
            f.write(content)
        print(f"Wrote {filepath}")

write_files(dir_09, cases_09)
write_files(dir_10, cases_10)
